public class ForEach {
    public static void main(String[] args){
        String[] names = {"Thanuja","Manasa","Usha"};
        for(String name: names){
            System.out.println("Name: "+name);
        }
    }
     

    
}
/*
 *  javac ForEach.java
java ForEach      
Name: Thanuja
Name: Manasa
Name: Usha
 */
